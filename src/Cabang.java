    private void jTblCabangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblCabangMouseClicked
        try {
            JTable source = (JTable)evt.getSource();
            int row = source.rowAtPoint( evt.getPoint() );
            String Kode=source.getModel().getValueAt(row, 1)+"";
            String nama=source.getModel().getValueAt(row, 2)+"";
            String lokasi=source.getModel().getValueAt(row, 3)+"";
            String id=source.getModel().getValueAt(row, 4)+"";
            jTxtId.setText(id);
            jTxtNama.setText(nama);
            jTxtKode.setText(Kode);
            jTxtLokasi.setText(lokasi);
            jBtnSimpan.setText("Rubah");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jTblCabangMouseClicked

