    private void jBtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            DatabaseConnection conn = new DatabaseConnection();
            String nama = jTxtNama.getText();
            String kode = jTxtKode.getText();
            String lokasi = jTxtLokasi.getText();
            int id = Integer.parseInt(jTxtId.getText());
            if(id == -1){
                String sql = "INSERT INTO cabang (kode, lokasi, nama) VALUES ('"+kode+"', '"+lokasi+"', '"+nama+"');";
                conn.cn.execute(sql);
            }else{
                String sql = "UPDATE cabang c SET kode = '"+kode+"', lokasi = '"+lokasi+"', nama = '"+nama+"' WHERE id = "+id+";";
                conn.cn.execute(sql);
            }
            this.getList();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jBtnSimpanActionPerformed
    private void resetForm(){
        jBtnSimpan.setText("Simpan");
        jTxtNama.setText("");
        jTxtId.setText("-1");
        jTxtId.hide();
    }
    private void getList(){
        try {
            this.resetForm();
            DatabaseConnection conn = new DatabaseConnection();
            String sql = "SELECT * FROM cabang p;";
            ResultSet rs = conn.cn.executeQuery(sql);
            
            DefaultTableModel tm = (DefaultTableModel)jTblCabang.getModel();
            tm.setRowCount(0);
            int i = 1;
            while(rs.next()){
                Object o[] = {i,rs.getString("kode"),rs.getString("nama"),rs.getString("lokasi"),rs.getInt("id")};
                tm.addRow(o);
                i = i + 1;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println(e.getMessage());
        }
    }
    private void jBtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDeleteActionPerformed
        try {
            DatabaseConnection conn = new DatabaseConnection();
            int input = JOptionPane.showConfirmDialog(null, "Apakah anda yakin menghapus "+jTxtNama.getText()+" ?","Pilih salah satu",JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
            if(input == 0){
                String sql = "DELETE FROM cabang WHERE id = "+jTxtId.getText()+";";
                conn.cn.execute(sql);                                
                this.getList();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println(e.getMessage());
        }	
    }//GEN-LAST:event_jBtnDeleteActionPerformed

