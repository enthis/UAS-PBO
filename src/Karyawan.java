    private void jTblKaryawanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTblKaryawanMouseClicked
        try {
            JTable source = (JTable)evt.getSource();
            int row = source.rowAtPoint( evt.getPoint() );
            String id=source.getModel().getValueAt(row, 1)+"";
            String nama=source.getModel().getValueAt(row, 2)+"";
            jTxtIdKaryawan.setText(id);
            jTxtNamaKaryawan.setText(nama);
            jBtnSimpanKaryawan.setText("Rubah");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jTblKaryawanMouseClicked

