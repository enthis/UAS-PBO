    private void jBtnSimpanKaryawanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSimpanKaryawanActionPerformed
        // TODO add your handling code here:
        try {
            DatabaseConnection conn = new DatabaseConnection();
            String nama = jTxtNamaKaryawan.getText();
            int id = Integer.parseInt(jTxtIdKaryawan.getText());

            if(nama == "" ){
                throw new Exception("Ada Field kosong");
            }
            if(id == -1){
                
                String sql = "INSERT INTO karyawan (nama)  VALUES ('"+nama+"');";
                conn.cn.execute(sql);
            }else{
                String sql = "UPDATE karyawan SET nama = '"+nama+"' WHERE id = "+id+";";
                conn.cn.execute(sql);
            }
            this.getListKaryawan();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jBtnSimpanKaryawanActionPerformed
    private void resetForm(){
        jBtnSimpanKaryawan.setText("Simpan");
        jTxtNamaKaryawan.setText("");
        jTxtIdKaryawan.setText("-1");
        jTxtIdKaryawan.hide();
    }
    private void getListKaryawan(){
        try {
            this.resetForm();
            DatabaseConnection conn = new DatabaseConnection();
            String sql = "SELECT * FROM karyawan p;";
            ResultSet rs = conn.cn.executeQuery(sql);
            
            DefaultTableModel tm = (DefaultTableModel)jTblKaryawan.getModel();
            tm.setRowCount(0);
            int i = 1;
            while(rs.next()){
                Object o[] = {i,rs.getInt("id"),rs.getString("nama")};
                tm.addRow(o);
                i = i + 1;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println(e.getMessage());
        }
    }
    private void jBtnDeleteKaryawanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDeleteKaryawanActionPerformed
        try {
            DatabaseConnection conn = new DatabaseConnection();
            int input = JOptionPane.showConfirmDialog(null, "Apakah anda yakin menghapus "+jTxtNamaKaryawan.getText()+" ?","Pilih salah satu",JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
            if(input == 0){
                String sql = "DELETE FROM karyawan WHERE id = "+jTxtIdKaryawan.getText()+";";
                conn.cn.execute(sql);                                
                this.getListKaryawan();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
            System.out.println(e.getMessage());
        }	
    }//GEN-LAST:event_jBtnDeleteKaryawanActionPerformed

